    /// <summary>Compute CRC-S3 (16-bit, poly 0x080F). Used for data CRC in base format long packets and time coordination.</summary>
    public static ushort ComputeS3(ReadOnlySpan<byte> data, ushort preset)
    {
        ushort crc = preset;
        foreach (byte b in data)
            crc = (ushort)(CrcS3Table[(crc >> 8) ^ b] ^ (crc << 8));
        return crc;
    }

    /// <summary>CRC-S3 over a single byte.</summary>
    public static ushort ComputeS3(byte data, ushort preset)
    {
        return (ushort)(CrcS3Table[(preset >> 8) ^ data] ^ (preset << 8));
    }

    /// <summary>CRC-S3 over a 16-bit value (little-endian byte order).</summary>
    public static ushort ComputeS3(ushort data, ushort preset)
    {
        ushort crc = preset;
        crc = (ushort)(CrcS3Table[(crc >> 8) ^ (data & 0xFF)] ^ (crc << 8));
        crc = (ushort)(CrcS3Table[(crc >> 8) ^ ((data >> 8) & 0xFF)] ^ (crc << 8));
        return crc;
    }

    // ==================== CRC-S4 (32-bit, poly 0xEDB88320 — standard CRC-32 reflected) ====================

    private static readonly uint[] CrcS4Table = GenerateCrcS4Table();

    private static uint[] GenerateCrcS4Table()
    {
        var table = new uint[256];
        for (uint i = 0; i < 256; i++)
        {
            uint crc = i;
            for (int j = 0; j < 8; j++)
                crc = (crc & 1) != 0 ? (crc >> 1) ^ 0xEDB88320 : crc >> 1;
            table[i] = crc;
        }
        return table;
    }

    /// <summary>Compute CRC-S4 (CRC-32). Used for CPCRC, SCCRC, and other configuration CRCs.</summary>
    public static uint ComputeS4(ReadOnlySpan<byte> data, uint preset = 0xFFFFFFFF)
    {
        uint crc = preset;
        foreach (byte b in data)
            crc = CrcS4Table[(crc ^ b) & 0xFF] ^ (crc >> 8);
        return crc;
    }

    // ==================== CRC-S5 (24-bit, poly 0x5D6DCB, XorOut 0x00FFFFFF) ====================

    private static readonly uint[] CrcS5Table = GenerateCrcS5Table();

    private static uint[] GenerateCrcS5Table()
    {
        const uint poly = 0x5D6DCB;
        var table = new uint[256];
        for (uint i = 0; i < 256; i++)
        {
            uint crc = i << 16;
            for (int j = 0; j < 8; j++)
                crc = (crc & 0x800000) != 0 ? (crc << 1) ^ poly : crc << 1;
            table[i] = crc & 0x00FFFFFF;
        }
        return table;
    }

    /// <summary>
    /// Compute CRC-S5 (24-bit, poly 0x5D6DCB) with XorOut applied.
    /// Use this for the FINAL CRC value that goes on the wire.
    /// For intermediate/seed computations, use ComputeS5Raw.
    /// </summary>
    public static uint ComputeS5(ReadOnlySpan<byte> data, uint preset)
    {
        return ComputeS5Raw(data, preset) ^ 0x00FFFFFF;
    }

    /// <summary>
    /// Compute CRC-S5 WITHOUT XorOut. Use for incremental/seed computation
    /// where the result feeds into the next CRC step.
    /// </summary>
    public static uint ComputeS5Raw(ReadOnlySpan<byte> data, uint preset)
    {
        uint crc = preset & 0x00FFFFFF;
        foreach (byte b in data)
            crc = CrcS5Table[((crc >> 16) ^ b) & 0xFF] ^ ((crc << 8) & 0x00FFFFFF);
        return crc;
    }

    // ==================== PID/CID Seed Computation ====================
    // Per CSS reference (IXCRCmisc.c), PID/CID byte stream layout is:
    //   vendId(2LE) + devSerNum(4LE) + cnxnSerNum(2LE) = 8 bytes
    // Init values are 0 (not 0xFF/0xFFFF) — see IXCRC_CrcS1compute(..., 0U)

    /// <summary>
    /// Compute CRC-S1 seed from a PID/CID.
    /// Parameters: vendorId, deviceSerialNumber, connectionSerialNumber.
    /// Byte stream: vendId(2LE) + devSerNum(4LE) + cnxnSerNum(2LE), init=0.
    /// </summary>
    public static byte PidCidSeedS1(ushort vendorId, uint deviceSerial, ushort connectionSerial)
    {
        Span<byte> buf = stackalloc byte[8];
        BinaryPrimitives.WriteUInt16LittleEndian(buf, vendorId);
        BinaryPrimitives.WriteUInt32LittleEndian(buf.Slice(2), deviceSerial);
        BinaryPrimitives.WriteUInt16LittleEndian(buf.Slice(6), connectionSerial);
        return ComputeS1(buf, 0);
    }

    /// <summary>Compute CRC-S3 seed from a PID/CID.</summary>
    public static ushort PidCidSeedS3(ushort vendorId, uint deviceSerial, ushort connectionSerial)
    {
        Span<byte> buf = stackalloc byte[8];
        BinaryPrimitives.WriteUInt16LittleEndian(buf, vendorId);
        BinaryPrimitives.WriteUInt32LittleEndian(buf.Slice(2), deviceSerial);
        BinaryPrimitives.WriteUInt16LittleEndian(buf.Slice(6), connectionSerial);
        return ComputeS3(buf, 0);
    }

    /// <summary>Compute CRC-S5 seed from a PID/CID. Returns raw (no XorOut) for use as seed.</summary>
    public static uint PidCidSeedS5(ushort vendorId, uint deviceSerial, ushort connectionSerial)
    {
        Span<byte> buf = stackalloc byte[8];
        BinaryPrimitives.WriteUInt16LittleEndian(buf, vendorId);
        BinaryPrimitives.WriteUInt32LittleEndian(buf.Slice(2), deviceSerial);
        BinaryPrimitives.WriteUInt16LittleEndian(buf.Slice(6), connectionSerial);
        return ComputeS5Raw(buf, 0);
    }

    /// <summary>Compute CRC-S3 from PID seed + rollover count (Extended Format).</summary>
    public static ushort PidRolloverSeedS3(ushort rolloverCount, ushort pidSeedS3)
    {
        return ComputeS3(rolloverCount, pidSeedS3);
    }

    /// <summary>Compute CRC-S5 from PID seed + rollover count (Extended Format). Returns raw seed.</summary>
    public static uint PidRolloverSeedS5(ushort rolloverCount, uint pidSeedS5)
    {
        Span<byte> buf = stackalloc byte[2];
        BinaryPrimitives.WriteUInt16LittleEndian(buf, rolloverCount);
        return ComputeS5Raw(buf, pidSeedS5);
    }
